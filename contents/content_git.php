<section class="section-main">
    <h3>Git</h3>
    <h4>Проверить версию:</h4>

    <p class="command-line">
        <span class="terminal">~$</span> <b>git version</b>
    </p>
	<div class="line"></div>
	<p class="command-line">
        <span class="terminal">~$</span> <b>git --version</b>
    </p>
    <div class="line"></div>
    <h4>Проверить глобальную конфигурацию:</h4>

    <p class="command-line">
        <span class="terminal">~$</span> <b>git config --global --list</b>
    </p>
    <div class="line"></div>
    <p class="command-line">
        <span class="terminal">~$</span> <b>git config --list --show-origin</b>
    </p>
    <div class="line"></div>
    <h4>Добавить пользователя в глобальную конфигурацию:</h4>

    <p class="command-line">
        <span class="terminal">~$</span> <b>git config --global user.name "John Doe"</b>
    </p>
    <div class="line"></div> 
    <h4>Добавить email пользователя в глобальную конфигурацию:</h4>

    <p class="command-line">
        <span class="terminal">~$</span> <b>git config --global user.email johndoe@example.com</b>
    </p>
    <div class="line"></div>
